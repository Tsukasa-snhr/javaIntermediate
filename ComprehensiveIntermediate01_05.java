public class ComprehensiveIntermediate01_05 { 
    public static void main(String[] args) { 
        for (int v = 1; v <= 9; v++) { 
            for (int h = 1; h <= 9; h++) { 
                System.out.print(v * h + "\t");
            }
            System.out.println(); 
        }
    }
}